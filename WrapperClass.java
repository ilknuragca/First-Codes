package day01try;

public class WrapperClass {
	
  public static void main(String[]args) {
	  //primitive data type==>Wrapper  AutoBoxing
	  //WrapperClass==> primitive      UnBoxing
	  //wrapper class= Boolean,Byte,Character,Short,Integer,Long,Float,Double
	  
	  int num1=Integer.MIN_VALUE;
	  System.out.println(num1);
	  
	  int num2=Integer.MAX_VALUE;
	  System.out.println(num2);
	  
	  int num3= Byte.MIN_VALUE;
	  System.out.println(num3);
	  
	  int num4=Byte.MAX_VALUE;
	  System.out.println(num4);
	  
	 double num5=Integer.MAX_VALUE;
	 System.out.println(num5);
	  
  }
}
